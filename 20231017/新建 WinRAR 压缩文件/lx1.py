# 列表
list = [1,2,3,4,5,6,7,8,9]

# 索引
print(list[0])
print(list[1])
print(list[2])
print(list[3])
print(list[4])
print(list[5])
print(list[4:])

# 结束索引
print(list[:])
print(list[:3])
print(list[:4])
print(list[:5])
print(list[:6])
print(list[:7])

# 区间索引
print(list[2:5])
print(list[2:])
print(list[:5])
print(list[:])

# 反向索引
print(list[-1])
print(list[-2])
print(list[-3])
print(list[-4])
print(list[-5])
print(list[-6])
print(list[-1::1])

# 省略索引
print(list[::2])
print(list[::3])
print(list[::4])
print(list[::5])
print(list[::6])
print(list[::7])

# 步长
print(list[::2])
print(list[::-1])
print(list[::-2])
print(list[-3::-2])
